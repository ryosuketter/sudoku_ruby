grid = [1,2,3,4,5]

puts "\n------"
print (0..4).map{ |i| i }
# [0, 1, 2, 3, 4]

puts "\n------"
print (0..4).map{ |i| grid[1*i, 9]}
# [[1, 2, 3, 4, 5], [2, 3, 4, 5], [3, 4, 5], [4, 5], [5]]

puts "\n------"
print (0..4).map{ |i| grid[1*i, 9].map{ |v| (v || '.')}}
# [[1, 2, 3, 4, 5], [2, 3, 4, 5], [3, 4, 5], [4, 5], [5]]

puts "\n------"
print (0..4).map{ |i| grid[1*i, 9].map{ |v| (v || '.')}.join("")}
# ["12345", "2345", "345", "45", "5"]

puts "\n------"
print (0..4).map{ |i| grid[1*i, 9].map{ |v| (v || '.')}.join("")}.join("\n")
# 12345
# 2345
# 345
# 45
# 5%

puts "\n------"
print (0..4).map{ |i| grid[1*i, 9].map{ |v| (v || '.')}.join("")}.join("\n"), "\n"
# 12345
# 2345
# 345
# 45
# 5